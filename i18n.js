const LANG_KEY = "portfolio.lang";
const SUPPORTED = ["en", "km", "zh", "ja"];

const detectInitialLang = () => {
  const stored = localStorage.getItem(LANG_KEY);
  if (stored && SUPPORTED.includes(stored)) return stored;
  const browser = (navigator.language || "en").toLowerCase();
  if (browser.startsWith("km")) return "km";
  if (browser.startsWith("zh")) return "zh";
  if (browser.startsWith("ja")) return "ja";
  return "en";
};

const applyI18n = (lang) => {
  const dict = translations[lang] || translations.en;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key] != null) el.textContent = dict[key];
  });
  document.documentElement.lang = lang;
  document.documentElement.dataset.lang = lang;
};

const initLangSwitcher = () => {
  const switcher = document.querySelector(".lang-switch");
  if (!switcher) return;
  const toggle = switcher.querySelector(".lang-toggle");
  const current = switcher.querySelector(".lang-current");
  const buttons = switcher.querySelectorAll("[data-lang]");

  const setLang = (lang) => {
    if (!SUPPORTED.includes(lang)) lang = "en";
    localStorage.setItem(LANG_KEY, lang);
    applyI18n(lang);
    if (current) current.textContent = lang.toUpperCase();
    buttons.forEach((b) => b.classList.toggle("active", b.dataset.lang === lang));
    switcher.classList.remove("open");
    toggle?.setAttribute("aria-expanded", "false");
  };

  toggle?.addEventListener("click", (e) => {
    e.stopPropagation();
    const open = switcher.classList.toggle("open");
    toggle.setAttribute("aria-expanded", open);
  });
  buttons.forEach((b) => {
    b.addEventListener("click", () => setLang(b.dataset.lang));
  });
  document.addEventListener("click", (e) => {
    if (!switcher.contains(e.target)) {
      switcher.classList.remove("open");
      toggle?.setAttribute("aria-expanded", "false");
    }
  });

  setLang(detectInitialLang());
};

window.addEventListener("DOMContentLoaded", initLangSwitcher);
